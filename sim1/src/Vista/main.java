    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (nombretext.getText().length() > 2) {
            if (comboTipo.getSelectedIndex() != 0) {
                try {

                    if (comboTipo.getSelectedIndex() == 1) {
                        Mayoristavista f = new Mayoristavista(nombretext.getText(), Inet4Address.getLocalHost().getHostAddress());
                        f.setVisible(true);
                        this.setVisible(false);
                    }
                    if (comboTipo.getSelectedIndex() == 2) {
                        MinoritaVista f = new MinoritaVista(nombretext.getText(), Inet4Address.getLocalHost().getHostAddress(), false);
                        f.setVisible(true);
                        this.setVisible(false);
                    }

                    if (comboTipo.getSelectedIndex() == 3) {
                        MinoritaVista f = new MinoritaVista(nombretext.getText(), Inet4Address.getLocalHost().getHostAddress(), true);
                        f.setVisible(true);
                        this.setVisible(false);
                    }
                     if (comboTipo.getSelectedIndex() == 4) {
                        ClienteVista f = new ClienteVista(nombretext.getText(), Inet4Address.getLocalHost().getHostAddress(), true);
                        f.setVisible(true);
                        this.setVisible(false);
                    }
                } catch (UnknownHostException ex) {
                    Logger.getLogger(main.class.getName()).log(Level.SEVERE, null, ex);
                } catch (IOException ex) {
                    Logger.getLogger(main.class.getName()).log(Level.SEVERE, null, ex);
                }
            } else {
                JOptionPane.showMessageDialog(null, "Elija Un Tipo");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Verifique el Nombre");
        }

    }//GEN-LAST:event_jButton1ActionPerformed

